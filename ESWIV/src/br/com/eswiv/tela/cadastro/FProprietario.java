    @Override
    public void inserirOuAlterar() {
        proprietario = new Proprietario();
        proprietario.setNome(tfNome.getText());
        proprietario.setCelular(tfCelular.getText());
        proprietario.setEmail(tfEmail.getText());
        proprietario.setData_cadastro(dtCadastro.getDate());
        proprietario.setTelefone(tfTelefone.getText());
        proprietario.setCpf(tfCpf.getText());
        Endereco endereco = new Endereco();
        endereco.setLogradouro(tfLogradouro.getText());
        endereco.setNumero(tfNumero.getText());
        endereco.setComplemento(tfComplemento.getText());
        endereco.setBairro(tfBairro.getText());
        endereco.setCep(tfCep.getText());
        endereco.setCidade(jsCidade.getValue());
        proprietario.setEndereco(endereco);
        try {
            if (tbAlterar.isSelected()) {
                proprietario.setCodigo(Integer.valueOf(tfCodigo.getText()));
                dao.alterar(proprietario);
            } else {
                dao.adicionar(proprietario);
            }
        } catch (RuntimeException ex) {
            LoggerEx.log(ex);
        }
    }

    @Override
    public void preencherCampos(IModelo m) {
        proprietario = (Proprietario) m;
        tfCodigo.setText(String.valueOf(proprietario.getCodigo()));
        tfNome.setText(proprietario.getNome());
        dtCadastro.setDate(proprietario.getData_cadastro());
        tfEmail.setText(proprietario.getEmail());
        tfTelefone.setText(proprietario.getTelefone());
        tfCelular.setText(proprietario.getCelular());
        tfCpf.setText(proprietario.getCpf());
        Endereco e = proprietario.getEndereco();
        tfLogradouro.setText(e.getLogradouro());
        tfNumero.setText(e.getNumero());
        tfComplemento.setText(e.getComplemento());
        tfBairro.setText(e.getBairro());
        tfCep.setText(e.getCep());
        Cidade c = e.getCidade();
        jsCidade.setValue(c, c != null ? c.getCodigo() : "");
    }

