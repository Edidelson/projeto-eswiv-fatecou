    private void jsCidadeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jsCidadeFocusLost
        if (jsCidade.getText().equals("")) {
            lbCidadeSelecionada.setText("");
            jsCidade.setValue(null, "");
            return;
        }
        DAOCidade daoCidade = new DAOCidade();
        String value = jsCidade.getEditor().getText();
        Cidade c = null;
        if (value.length() > 0) {
            c = daoCidade.consultar(Integer.valueOf(value), false);
        }
        jsCidade.setValue(c, c != null ? c.getCodigo() : "");
    }//GEN-LAST:event_jsCidadeFocusLost

