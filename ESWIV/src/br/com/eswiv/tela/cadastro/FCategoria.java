    private javax.swing.JPanel plCamposCategoria;
    private javax.swing.JToggleButton tbAlterar;
    private com.zap.arca.JATable tbCategoria;
    private javax.swing.JToggleButton tbIncluir;
    private com.zap.arca.JATextField tfCodigo;
    private com.zap.arca.JATextField tfDescricao;
