    @Override
    public void iniciar() {
        dao = new DAOCategoria();

        tbPrincipal = tbCategoria;
        toggleButton = tbIncluir;
        ctChave = tfCodigo;

        tbCategoria.setName("TB_FCATEGORIA");
        tbCategoria.setModel(categoriaTableModel);

        camposVerificar = new Component[]{tfDescricao};
        camposLimpar = new Component[]{tfDescricao, tfCodigo};

        WindowUtils.nextEnter(plCamposCategoria);
        WindowUtils.exitEsc(this);
        configurarSincronizacao(dao, tbCategoria);
    }

    @Override
    public void inserirOuAlterar() {
        categoria = new Categoria();
        categoria.setDescricao(tfDescricao.getText());
        try {
            if (tbAlterar.isSelected()) {
                categoria.setCodigo(Integer.valueOf(tfCodigo.getText()));
                dao.alterar(categoria);
            } else {
                dao.adicionar(categoria);
            }
        } catch (RuntimeException ex) {
            LoggerEx.log(ex);
        }
    }

    @Override
    public void preencherCampos(IModelo m) {
        categoria = (Categoria) m;
        tfCodigo.setText(String.valueOf(categoria.getCodigo()));
        tfDescricao.setText(categoria.getDescricao());
    }

